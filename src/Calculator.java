    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed
        // TODO add your handling code here:
        if(list.getSelectedItem().equals("select an option") || t1.getText().equals("")|| t2.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"You must select an option and insert the values","Error Message",JOptionPane.INFORMATION_MESSAGE);
            
        }
        else if(list.getSelectedItem().equals("Distance"))
        {
            double speed = Double.parseDouble(t1.getText());
            double time = Double.parseDouble(t2.getText());
            double distance = speed*time;
            
            String value = String.format("%.2f", distance);
            l2.setText(value);
            
            l1.setText("        Distance = Speed * Time       ");
        }
        else if(list.getSelectedItem().equals("Speed"))
        {
            double Distance = Double.parseDouble(t1.getText());
            double time = Double.parseDouble(t2.getText());
            double Speed = Distance/time;
            
            String value = String.format("%.2f", Speed);
            l2.setText(value);
            
            l1.setText("        Speed = Distance / Time       ");
        }
        else if(list.getSelectedItem().equals("Time"))
        {
            double distance = Double.parseDouble(t1.getText());
            double speed = Double.parseDouble(t2.getText());
            double time = distance/speed;
            
            String value = String.format("%.2f", time);
            l2.setText(value);
            
            l1.setText("        time = Distance / Speed       ");
        }
    
    }//GEN-LAST:event_b1ActionPerformed

